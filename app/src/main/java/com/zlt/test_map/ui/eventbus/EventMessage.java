package com.zlt.test_map.ui.eventbus;

public class EventMessage {
    private String token;
    private int id;

    public EventMessage(String token, int id) {
        this.token = token;
        this.id = id;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
